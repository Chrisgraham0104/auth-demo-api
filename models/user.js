const mongoose = require("mongoose");
const MainSchema = mongoose.Schema;
// Create Schema
const UserSchema = new MainSchema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});
module.exports = User = mongoose.model("users", UserSchema);